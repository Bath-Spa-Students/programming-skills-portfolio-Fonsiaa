#Q4: Compute the area of a triangle

import math #python access another module
base = float(input("Input a number for your triangle's base:"))#asks user to input a number
height = float(input("Input a number for your triangle's height:"))#asks user to input a number
#formula to compute for the area of the triangle
area = 0.5 * base * height #calculates the input from the user
print("The area of your triangle is", area) #print the calculation of the triangle
